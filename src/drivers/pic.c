#include "pic.h"
#include "common.h"

void pic_init() {
    outb(0x20, 0x11); outb(0xA0, 0x11);
    outb(0x21, 0x20); outb(0xA1, 0x28);
    outb(0x21, 0x04); outb(0xA1, 0x02);
    outb(0x21, 0x01); outb(0xA1, 0x01);

    // Tüm IRQ'lar açık
    outb(0x21, 0x00);
    outb(0xA1, 0x00);
}

void pic_send_eoi(unsigned char int_no) {
    if (int_no >= 40) outb(0xA0, 0x20);
    outb(0x20, 0x20);
}
